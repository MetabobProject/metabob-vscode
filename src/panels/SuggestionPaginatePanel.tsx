import { useCallback } from 'react'
import { useUser } from '../hooks/useUser'
import { usePagination } from 'react-use-pagination'

export const SuggestionPaginatePanel = () => {
  const {
    showSuggestionPaginatePanel,
    suggestion,
    initialState,
    userQuestionAboutSuggestion,
    isSuggestionRegenerateLoading,
    setIsSuggestionRegenerateLoading,
    suggestionPaginationRegenerate,
    setSuggestion,
    setUserQuestionAboutSuggestion
  } = useUser()
  const { currentPage, setNextPage, setPreviousPage, nextEnabled, previousEnabled } = usePagination({
    totalItems: suggestionPaginationRegenerate.length,
    initialPage: 0,
    initialPageSize: 1
  })

  const handleSuggestionRegenerate = useCallback(() => {
    setIsSuggestionRegenerateLoading(true)
    vscode.postMessage({
      type: 'onSuggestionClicked',
      data: {
        input: userQuestionAboutSuggestion,
        initData: { ...initialState }
      }
    })
  }, [suggestion, initialState])

  const handleBackPaginateChange = useCallback(
    (e: any) => {
      e.preventDefault()
      setPreviousPage()
      const item = suggestionPaginationRegenerate[currentPage - 1]
      setSuggestion(item?.description || '')
      setUserQuestionAboutSuggestion(item?.question || '')
    },
    [currentPage, setSuggestion, setUserQuestionAboutSuggestion, setPreviousPage]
  )

  const handleNextPaginateChange = useCallback(
    (e: any) => {
      e.preventDefault()
      setNextPage()
      const item = suggestionPaginationRegenerate[currentPage + 1]
      setSuggestion(item?.description || '')
      setUserQuestionAboutSuggestion(item?.question || '')
    },
    [currentPage, setSuggestion, setUserQuestionAboutSuggestion, setNextPage]
  )

  if (showSuggestionPaginatePanel) {
    return (
      <>
        <div className='flex flex-row items-center'>
          <div className='inline-flex mt-2 xs:mt-0'>
            <button
              disabled={!previousEnabled}
              onClick={handleBackPaginateChange}
              className={
                !previousEnabled
                  ? `opacity-25 cursor-not-allowed inline-flex items-center px-4 py-2 text-sm font-medium text-white bg-gray-800 rounded-l hover:bg-gray-900 dark:bg-gray-800 dark:border-gray-700 dark:text-gray-400 dark:hover:bg-gray-700 dark:hover:text-white`
                  : `inline-flex items-center px-4 py-2 text-sm font-medium text-white bg-gray-800 rounded-l hover:bg-gray-900 dark:bg-gray-800 dark:border-gray-700 dark:text-gray-400 dark:hover:bg-gray-700 dark:hover:text-white`
              }
            >
              <svg
                aria-hidden='true'
                className='w-5 h-5 mr-2'
                fill='currentColor'
                viewBox='0 0 20 20'
                xmlns='http://www.w3.org/2000/svg'
              >
                <path
                  fill-rule='evenodd'
                  d='M7.707 14.707a1 1 0 01-1.414 0l-4-4a1 1 0 010-1.414l4-4a1 1 0 011.414 1.414L5.414 9H17a1 1 0 110 2H5.414l2.293 2.293a1 1 0 010 1.414z'
                  clip-rule='evenodd'
                ></path>
              </svg>
            </button>
            <button
              onClick={handleSuggestionRegenerate}
              className='inline-flex items-center px-4 py-2 text-sm font-medium text-white bg-gray-800 rounded-l hover:bg-gray-900 dark:bg-gray-800 dark:border-gray-700 dark:text-gray-400 dark:hover:bg-gray-700 dark:hover:text-white'
            >
              {isSuggestionRegenerateLoading ? (
                <>
                  <svg
                    aria-hidden='true'
                    role='status'
                    className='inline w-4 h-4 mr-3 text-white animate-spin'
                    viewBox='0 0 100 101'
                    fill='none'
                    xmlns='http://www.w3.org/2000/svg'
                  >
                    <path
                      d='M100 50.5908C100 78.2051 77.6142 100.591 50 100.591C22.3858 100.591 0 78.2051 0 50.5908C0 22.9766 22.3858 0.59082 50 0.59082C77.6142 0.59082 100 22.9766 100 50.5908ZM9.08144 50.5908C9.08144 73.1895 27.4013 91.5094 50 91.5094C72.5987 91.5094 90.9186 73.1895 90.9186 50.5908C90.9186 27.9921 72.5987 9.67226 50 9.67226C27.4013 9.67226 9.08144 27.9921 9.08144 50.5908Z'
                      fill='#E5E7EB'
                    />
                    <path
                      d='M93.9676 39.0409C96.393 38.4038 97.8624 35.9116 97.0079 33.5539C95.2932 28.8227 92.871 24.3692 89.8167 20.348C85.8452 15.1192 80.8826 10.7238 75.2124 7.41289C69.5422 4.10194 63.2754 1.94025 56.7698 1.05124C51.7666 0.367541 46.6976 0.446843 41.7345 1.27873C39.2613 1.69328 37.813 4.19778 38.4501 6.62326C39.0873 9.04874 41.5694 10.4717 44.0505 10.1071C47.8511 9.54855 51.7191 9.52689 55.5402 10.0491C60.8642 10.7766 65.9928 12.5457 70.6331 15.2552C75.2735 17.9648 79.3347 21.5619 82.5849 25.841C84.9175 28.9121 86.7997 32.2913 88.1811 35.8758C89.083 38.2158 91.5421 39.6781 93.9676 39.0409Z'
                      fill='currentColor'
                    />
                  </svg>
                </>
              ) : (
                <>regenerate</>
              )}
            </button>
            <button
              disabled={!nextEnabled}
              onClick={handleNextPaginateChange}
              className={
                !nextEnabled
                  ? `opacity-25 cursor-not-allowed inline-flex items-center px-4 py-2 text-sm font-medium text-white bg-gray-800 rounded-l hover:bg-gray-900 dark:bg-gray-800 dark:border-gray-700 dark:text-gray-400 dark:hover:bg-gray-700 dark:hover:text-white`
                  : `inline-flex items-center px-4 py-2 text-sm font-medium text-white bg-gray-800 rounded-l hover:bg-gray-900 dark:bg-gray-800 dark:border-gray-700 dark:text-gray-400 dark:hover:bg-gray-700 dark:hover:text-white`
              }
            >
              <svg
                aria-hidden='true'
                className='w-5 h-5 ml-2'
                fill='currentColor'
                viewBox='0 0 20 20'
                xmlns='http://www.w3.org/2000/svg'
              >
                <path
                  fill-rule='evenodd'
                  d='M12.293 5.293a1 1 0 011.414 0l4 4a1 1 0 010 1.414l-4 4a1 1 0 01-1.414-1.414L14.586 11H3a1 1 0 110-2h11.586l-2.293-2.293a1 1 0 010-1.414z'
                  clip-rule='evenodd'
                ></path>
              </svg>
            </button>
          </div>
        </div>
      </>
    )
  } else {
    return <></>
  }
}
